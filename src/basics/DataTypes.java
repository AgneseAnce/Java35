package basics;

public class DataTypes {
    public static void main(String[] args) {
        //Integer Data Types
        //byte -127 to 127
        byte num1 = 100;
        short num2 = 1000;
        long num4 = 32145588;
        long num5 = 1255878521L;

        //Floating point data type
        float num3 = 12312.1254f;
        double num6 = 12354.123664478874d;

        //Boolean data type
        boolean redLightSwitch = false;
        boolean greenLightSwitch = true;

        //Character data type
        char char_1 = '%';
        char char2 = 'w';
        char char3 = '0';
        char char4 = '\n';

        //String
        String text1 = "This is SPARTAAAAA!";

        //Print out of variables
        System.out.println(num1);
        System.out.println(num6);
        System.out.println(redLightSwitch);
        System.out.println(greenLightSwitch);
        System.out.println(char4);
        System.out.println(char2);
        System.out.println(text1);
    }
}
